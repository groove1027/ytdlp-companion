// API 호출
// ============================================================
async function registerUser(entry) {
  const password = entry.tier === 'premium' ? 'switching-premium' : 'switching-basic';
  const inviteCode = entry.tier === 'premium' ? 'PREMIUM' : 'BASIC';

  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 30000); // 30초 타임아웃

  try {
    const res = await fetch(`${PRODUCTION_URL}/api/auth/signup`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        email: entry.email,
        password,
        inviteCode,
        displayName: entry.nickname,
      }),
      signal: controller.signal,
    });

    let data;
    try {
      data = await res.json();
    } catch {
      data = { error: `Non-JSON response (${res.status})` };
    }
    return { status: res.status, data };
  } finally {
    clearTimeout(timeout);
  }
}

