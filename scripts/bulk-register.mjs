// 이메일 유효성 검사
// ============================================================
function isValidEmail(email) {
  if (!email || typeof email !== 'string') return false;
  const trimmed = email.trim();
  // 기본: @와 . 포함, 한글 없음, 최소 길이
  return /^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(trimmed) && !/[가-힣]/.test(trimmed);
}

// ============================================================
// 데이터 정제
// ============================================================
function processData() {
  const valid = [];
  const invalid = [];

  for (const row of RAW_DATA) {
    const [timestamp, nickname, name, tierRaw, emailRaw, phone] = row;
    const email = emailRaw.trim().toLowerCase();
    const tier = tierRaw.includes('프리미엄') ? 'premium' : 'basic';

    if (!isValidEmail(emailRaw.trim())) {
      invalid.push({ timestamp, nickname, name, tier: tierRaw, emailRaw: emailRaw.trim(), phone });
      continue;
    }

    valid.push({ timestamp, nickname: nickname.trim(), name: name.trim(), tier, email, phone });
  }

  // 1단계: 같은 사람(닉네임+성함)이 이메일을 변경한 경우 → 최신 것만 유지
  // 이렇게 하면 이전 이메일 행이 제거됨
  const personMap = new Map();
  for (const entry of valid) {
    const personKey = `${entry.nickname}::${entry.name}`;
    personMap.set(personKey, entry); // 마지막(최신) 것이 남음
  }
  const personDeduplicated = Array.from(personMap.values());

  // 2단계: 같은 이메일이 여전히 중복이면 → 최신 것만 유지
  const emailMap = new Map();
  for (const entry of personDeduplicated) {
    emailMap.set(entry.email, entry);
  }

  const deduplicated = Array.from(emailMap.values());

  return { deduplicated, invalid };
}

